#include "minishell.h"

void	chdir_(char *dir)
{
	int	status;

	status = chdir(dir);
	if (status != 0)
	{
		ft_putstr_fd("cd: ", 2);
		ft_putstr_fd(dir, 2);
		ft_putstr_fd(": ", 2);
		ft_putstr_fd(strerror(errno), 2);
		ft_putstr_fd("\n", 2);
	}
}
