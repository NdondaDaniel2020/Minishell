#include "minishell.h"

void	*ft_realloc(void *ptr, size_t original_size, size_t new_size)
{
	void	*new_ptr;

	if (new_size == 0)
	{
		free(ptr);
		return (NULL);
	}
	if (ptr == NULL)
	{
		return (malloc(new_size));
	}
	new_ptr = malloc(new_size);
	if (new_ptr == NULL)
	{
		return (NULL);
	}
	if (original_size > new_size)
	{
		original_size = new_size;
	}
	ft_memmove(new_ptr, ptr, original_size);
	free(ptr);
	return (new_ptr);
}
